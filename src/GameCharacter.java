public class GameCharacter {
    int attack;
    int defense;
    int health;
    int speed;
    int ppSize;
    public GameCharacter(int attack, int defense, int health, int speed, int ppSize){
        this.attack = attack;
        this.defense = defense;
        this.health = health;
        this.speed = speed;
        this.ppSize = ppSize;
    }//constructor

}
